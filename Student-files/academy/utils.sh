############################################################
# Load Current Student
############################################################

load_student() {

    STUDENT=$(tail -n +2 "$BASE_DIR/database/students.csv" | head -n 1)

    IFS=',' read ID NAME SURNAME USER LEVEL XP STAGE <<< "$STUDENT"

}
############################################################
# Count Missions
############################################################

total_missions() {

    tail -n +2 "$BASE_DIR/database/missions.csv" | wc -l

}
############################################################
# Completed Missions
############################################################

completed_missions() {

    grep -c "Completed" "$BASE_DIR/database/missions.csv"

}
############################################################
# Calculate Progress
############################################################

progress_percentage() {

    TOTAL=$(total_missions)
    DONE=$(completed_missions)

    if [ "$TOTAL" -eq 0 ]; then
        echo 0
    else
        echo $((DONE * 100 / TOTAL))
    fi

}
############################################################
# Current Rank
############################################################

student_rank() {

    load_student

    if [ "$XP" -lt 100 ]; then
        echo "Beginner"

    elif [ "$XP" -lt 300 ]; then
        echo "Apprentice"

    elif [ "$XP" -lt 600 ]; then
        echo "Intermediate"

    elif [ "$XP" -lt 1000 ]; then
        echo "Advanced"

    else
        echo "Expert"
    fi

}
############################################################
# Welcome Message
############################################################

welcome() {

    load_student

    banner

    echo
    echo "Welcome $NAME $SURNAME"
    echo "Level : $LEVEL"
    echo "XP    : $XP"
    echo

}