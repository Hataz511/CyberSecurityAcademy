#!/bin/bash
BASE_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
source "$BASE_DIR/assets/colors.conf"

############################################################
# Cyber Security Academy
# Utility Functions v1.0
############################################################

############################################################
# Print separator line
############################################################

print_line() {
    printf '%*s\n' "${COLUMNS:-60}" '' | tr ' ' '='
}

############################################################
# Print academy title
############################################################

title() {

    clear

    print_line
    echo "           Cyber Security Academy"
    print_line
    echo

}

############################################################
# Success Message
############################################################

success() {

    echo
    echo -e "${GREEN}${BOLD}[SUCCESS]${RESET} $1"
    echo

}

############################################################
# Error Message
############################################################

error() {

    echo
    echo -e "${RED}${BOLD}[ERROR]${RESET} $1"
    echo

}
############################################################
# Information Message
############################################################

info() {

    echo
    echo -e "${CYAN}[INFO]${RESET} $1"
    echo

}

############################################################
# Loading Animation
############################################################

loading() {

    echo -n "$1"

    for i in {1..3}
    do
        sleep 0.5
        echo -n "."
    done

    echo

}

############################################################
# Pause
############################################################

pause() {

    echo
    read -p "Press ENTER to continue..."

}

############################################################
# Generate Random Student ID
############################################################

random_student_id() {

    openssl rand -hex 6

}

############################################################
# Generate Random Mission Flag
############################################################

random_flag() {

    echo "ACADEMY-$(openssl rand -hex 8)"

}

############################################################
# Academy Folder
############################################################

academy_folder() {

    echo "$HOME/.academy"

}

############################################################
# CyberLab Folder
############################################################

lab_folder() {

    echo "$HOME/CyberLab"

}

############################################################
# Create Folder If Missing
############################################################

create_folder() {

    if [ ! -d "$1" ]; then
        mkdir -p "$1"
    fi

}

############################################################
# Check File Exists
############################################################

file_exists() {

    if [ -f "$1" ]; then
        return 0
    else
        return 1
    fi

}

############################################################
# Check Folder Exists
############################################################

folder_exists() {

    if [ -d "$1" ]; then
        return 0
    else
        return 1
    fi

}

############################################################
# Current Date
############################################################

current_date() {

    date +"%Y-%m-%d"

}

############################################################
# Current Time
############################################################

current_time() {

    date +"%H:%M:%S"

}

############################################################
# Current DateTime
############################################################

current_datetime() {

    date +"%Y-%m-%d %H:%M:%S"

}

